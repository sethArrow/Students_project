		<h3>Research Topic: Risk Reporting: A Study of Risk Disclosures in the Annual Reports of Firms in Ghana</h3>
		<h4>Research Summary:</h4>
		<p>Organisations use their annual report as the major means of communicating about their value to shareholders and stakeholders. According 
		to Gray et al. (1995) annual reports serve as the official and legal means by which organisations regularly communicate with its stakeholders. 
		The annual report of organisations contains both the traditional financial statements and non-financial information. In the preparation of 
		financial statements, accountants consider the decision needs of the users of financial statements in order to present information that is 
		useful (Scott, 2003). Scott (2003) further explains that the rational risk-averse investor is interested in the riskiness of the firm in 
		making his economic buy or sell decision. Investors need to understand the risks a company takes to create value and they want to have information 
		on the sustainability of current value-creation strategies (Beretta and Bozzolan, 2004). Hence the disclosure of the various types of risk faced by 
		firms has become important. This concept of risk and its management has attracted attention lately however in depth research into this area is lacking 
		(Linsley and Shrives, 2006). Moreover, most research in risk disclosure is in developed countries (Linsley & Shrives (2006), Beretta & Bozzolan (2004), 
		Lajili & Zeghal (2005)). In Ghana, literature on the disclosure of risk by firms is scarce. This study intends to close this gap in literature. Also, 
		while most of the research used single years, this study will use a longitudinal approach in order to observe trends in the enhancement of risk disclosures.
		</p>
		<h4>Objectives</h4>
		<p>The Study will seek to achieve the following specific objectives;
		<ul>
			<li>To identify the nature and types of risk disclosed by listed firms </li>
