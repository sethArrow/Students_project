			<h3>Research Topic: Internal Audit Quality and Earnings Management</h3>
			<h4>Research Summary:</h4>
			<p>Various researchers have examined a lot of issues concerning earnings management in recent times especially 
			after the recent financial scandals around the world such as the Enron and WorldCom scandals (Healy and Wahlen 
			1999, Davidson et. al 2005 Prawitt et. al 2008).</p>
			<p>The basic objective of financial statements is the provision of information about the financial strength, 
			performance and changes in financial position of an enterprise that is useful to a wide range of users in 
			making economic decisions (IASC, 1989, paragraph 12) but there exist biases in the preparation of these 
			financial statements due to choice of accounting policies.</p>
			<p>Earnings management (EM) to SEC (1999, final rule) as cited in Davidson et al (2005) is the practice of 
			distorting the true financial performance of a company. To Clikeman et al (2001) earnings management is 
			the practice of making discretionary accounting choices or timing operating decisions to move reported 
			earnings toward a desired goal. Healy and Wahlen (1999) adds that EM occurs when managers use judgement 
			in financial reporting and in structuring transactions to alter financial reporting to mislead some 
			stakeholders about the underlying economic outcomes that depends on reported accounting numbers.</p>
			<p>Research has shown that management's forecasts are more biased when it is relatively difficult to detect 
			misrepresentation (Rogers and Stocken 2005). To Scott and Pitman (2009), the GAAP accrual accounting system 
			requires managers to make numerous accounting judgments that have a profound impact on reported earnings. 
			With such possible judgements, there is the likelihood for managers to manage reported earnings to achieve 
			their own goals, hence the need for auditors. Kim et al (2003), assert that, high quality auditors are more 
			likely to restrict earnings management. Internal Audit Function (IAF) play an incremental role in the quality 
			of work by external auditors. With this corporate governance (CG) in recent years has focused its attention to 
			the role of maintaining transparency and accountability of financial reporting (Al-Shetwi et al, 2011), 
			resulting in new acts of CG to improve financial reporting quality (Cohen et al., 2008). This brought the 
			introduction of internal audit as one of the four cornerstones of CG (Gramling et al., 2004; Prawitt et al., 2009). 
			The IAF is considered an effective function in governance structure, providing an important role in assessing internal 
			control effectiveness and also on the quality of financial reporting. With this, we seek to investigate and submit a 
			report on the impact IAF quality on earnings management in the case of Ghana.</p>
			<h4>Objectives of the Study</h4>
			<p>The research seeks to determine the level of quality of IAF in Ghanaian listed firms. In addition to find out the 
			extent of earnings management in our listed companies and also to identify the motivating factors of EM in Ghana if any. 
			Finally, the study seeks to find out the impact IAF quality has on earnings management in the case of Ghanaian listed non 
			financial institutions.</p>
			<h4>Scope</h4>
			<p>The research is limited to companies listed on the GSE from 2006 to 2010 and further to listed non financial firms on the GSE.</p>
			<h4>Methodology</h4>
			<p>A composite measure would be used in measuring IAF quality, which would then be correlated with EM (proxy by discretionary accrual). 
			In the formulation of IAF components, Prawitt et al (2008) components of the IAF quality is adopted, which is also a measure used 
			by the external auditing standards for external auditors to measure the quality of IAF (AICPA 1997). Both primary and secondary data 
			would be used in this study.</p>
