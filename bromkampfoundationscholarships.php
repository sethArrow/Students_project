		order to draw valid conclusions using content analysis, the coding method will have to be reliable (Linsley and Shrives, 2006).
		</p>
		<h4>Methodology</h4>
		<p>The annual reports of listed firms between 2006 and 2010 will be carefully analysed to identify statements of risk disclosures for each year. 
		Observations can then be made to identify any trends. A regression will then be used to test the relationship between the number of disclosures and 
		firm characteristics such as size, age, leverage and auditor type.
		</p>
